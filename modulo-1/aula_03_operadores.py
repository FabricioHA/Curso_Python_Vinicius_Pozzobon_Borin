#Em python, temos operadores matemáricos, como:

print(2+3)#Operador de soma
print(2-3)#Operador de subtração
print(2*3)#Operador de multiplicação
print(4**3)#Operador de potencialização (O quatro será multiplicado 3 vezes por ele mesmo)
print(2/3)#Operador de divisão
print(2//5)#Operador de divisão mostrando apenas o valor inteiro da divisão, desconsiderando o decimal
print(2%5)#Operador mostrando apenas o resto da divisão

